const SUPPORTED: Lang[] = ["fr", "en", "de", "es", "it", "pt", "nl", "ja", "zh"];

let _current = $state<Lang>("fr");

export function getLang(): Lang {
  return _current;
}

export function setLang(l: string) {
  const safe = (SUPPORTED.includes(l as Lang) ? l : "en") as Lang;
  _current = safe;
}

/** Renvoie la traduction pour la clé `k`, avec fallback EN puis clé brute. */
export function t(k: string): string {
  const cat = CATALOGS[_current];
  if (cat && cat[k]) return cat[k];
  const en = CATALOGS.en[k];
  if (en) return en;
  return k;
}
